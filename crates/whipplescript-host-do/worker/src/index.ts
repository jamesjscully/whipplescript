// The Cloudflare Worker + Durable Object shell for the WhippleScript runtime
// (DR-0033 chunk 5d). The Rust core is compiled to wasm and exposes
// `WasmDurableInstance` (create / step / status); this shell supplies the three
// pieces of JS glue the boundary needs and NOTHING ELSE -- no scheduling logic:
//
//   1. a `DoSqlBridge` over the object's SQLite (`state.storage.sql`),
//   2. the `fetch` for each `needs_http` round the core suspends on, and
//   3. JSON marshalling of the step protocol.
//
// The core is sans-IO: `step()` returns either a request to `fetch` (then call
// `step` again with the response) or a terminal. Because the in-flight effect is
// held inside the wasm instance AND the durable state lives in DO SQLite, an
// isolate eviction between rounds loses nothing -- a later request rehydrates.
//
// Aligns with Cloudflare docs: DO SQLite storage API (`ctx.storage.sql.exec`),
// positional-parameter binding, and `new_sqlite_classes` migrations (wrangler.toml).

// Generated by `wasm-bindgen --target bundler` (see README). Workers differ
// from webpack-style bundlers: importing a `.wasm` yields a WebAssembly.Module
// (NOT an instantiated exports object), so the pkg's self-initializing entry
// module cannot run under workerd. We bypass it and instantiate here: the
// module's single import namespace is the generated `_bg.js` glue itself.
import wasmModule from "../pkg/whipplescript_host_do_bg.wasm";
import * as bindings from "../pkg/whipplescript_host_do_bg.js";
import { WasmDurableInstance, __wbg_set_wasm } from "../pkg/whipplescript_host_do_bg.js";
import { Container, getRandom } from "@cloudflare/containers";

const wasmInstance = new WebAssembly.Instance(wasmModule, {
  "./whipplescript_host_do_bg.js": bindings,
});
__wbg_set_wasm(wasmInstance.exports);
(wasmInstance.exports as { __wbindgen_start?: () => void }).__wbindgen_start?.();

export interface Env {
  WORKFLOW_INSTANCE: DurableObjectNamespace;
  // Secrets injected as DO bindings -- the coerce/agent provider credentials the
  // sans-IO core asks for via `needs_http`. Wired into DurableEffectPorts in a
  // follow-on once the Rust `create` accepts them (see README "Effect creds").
  ANTHROPIC_API_KEY?: string;
  OPENAI_API_KEY?: string;
  // Dev/test override for the provider endpoint (e.g. a local mock in
  // `wrangler dev`); production omits it and uses the real Anthropic API.
  WHIP_PROVIDER_BASE_URL?: string;
  // Required shared secret for the public Worker control plane. Requests must
  // carry `Authorization: Bearer <token>` (or `x-whip-control-token` for local
  // clients that cannot set auth headers).
  WHIP_CONTROL_TOKEN?: string;
  // Secret shared with the executor/turn container sidecar. The DO sends it as
  // a bearer token on in-cluster calls; the sidecar rejects non-loopback calls
  // without it.
  WHIP_EXECUTOR_TOKEN?: string;
  // Class-A executor sidecar (compute plane P8): where exec.command effects
  // go as whip-executor/1 HTTP rounds. Unset = exec effects error. URLs on
  // this host are routed to the EXECUTOR container pool, not the network.
  WHIP_EXECUTOR_URL?: string;
  // Environment component of the delta-kernel content key: the executor
  // image identity (image digest = environment hash, §5 of the compute-plane
  // note). `whip deploy` computes it from the Dockerfile + whip binary and
  // injects it, so a toolchain bump is a visible warm-start epoch that rolls
  // the cache. Unset = the "do-v0" default epoch.
  WHIP_COMPUTE_ENV_HASH?: string;
  // Class-B turn containers (compute plane P8): agent turns run whole in a
  // per-turn container over whip-turn/1. URLs on this sentinel host route to
  // a per-turn container instance (idFromName over the turn id) — the 1:1
  // container-per-turn pattern. Unset = agent turns use the in-DO machine.
  WHIP_TURN_URL?: string;
  // Deploy-shipped AGENTS.md/CLAUDE.md and script capabilities. These are
  // operator configuration, not public runtime input.
  WHIP_PROJECT_CONTEXT_JSON?: string;
  WHIP_SCRIPT_CAPABILITIES_JSON?: string;
  EXECUTOR: DurableObjectNamespace<ExecutorContainer>;
}

// One Class-A executor instance (compute plane P8): a container running
// `whip executor` (whip-executor/1), paired 1:1 with this controlling DO.
// Stateless by design — hermeticity is demanded and audited, which is what
// makes the pool shareable. Egress default-deny is the container posture:
// the executor only ever answers this worker's in-cluster fetches.
export class ExecutorContainer extends Container {
  defaultPort = 8080;
  sleepAfter = "10m";
}

// Fixed pool size until platform autoscaling ships (manual knob, working
// zero-config default). `getRandom` is location-blind today — accepted.
const EXECUTOR_POOL_SIZE = 4;
const MAX_BOOTSTRAP_BYTES = 1024 * 1024;

// The DO schema (33 tables) as a bundled text module (wrangler.toml `rules`).
import DO_SCHEMA from "../do_schema.sql";

// Builtin capability seeds, mirroring the native migration-0001 builtin rows
