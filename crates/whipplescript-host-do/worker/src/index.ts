// Idempotent first-touch bootstrap: a fresh DO has an empty SQLite; apply the
// schema + builtin seeds exactly once (schema_migrations doubles as the marker).
function ensureSchema(sql: SqlStorage): void {
  const marker = sql
    .exec(`SELECT name FROM sqlite_master WHERE type = 'table' AND name = 'schema_migrations'`)
    .toArray();
  if (marker.length > 0) {
    return;
  }
  sql.exec(DO_SCHEMA);
  for (const seed of BUILTIN_SEEDS) {
    sql.exec(seed);
  }
}

// Row objects from `sql.exec` are column-name keyed; the Rust `DoSql` contract
// wants each row as a positional array in SELECT order. Cloudflare preserves
// column order in the returned objects, so `Object.values` reconstructs it.
function rowsToPositionalJson(cursor: Iterable<Record<string, unknown>>): string {
  const rows: unknown[][] = [];
  for (const row of cursor) {
    rows.push(Object.values(row));
  }
  return JSON.stringify(rows);
}

// `state.storage.sql` as the `DoSqlBridge` the wasm core imports. Params arrive as
// a JSON array of `null | number | string` (the marshalled `SqlValue`s).
function makeBridge(sql: SqlStorage) {
  return {
    exec(query: string, paramsJson: string): number {
      const params = JSON.parse(paramsJson) as unknown[];
      const cursor = sql.exec(query, ...params);
      return cursor.rowsWritten;
    },
    query(query: string, paramsJson: string): string {
      const params = JSON.parse(paramsJson) as unknown[];
      const cursor = sql.exec(query, ...params);
      return rowsToPositionalJson(cursor);
    },
  };
}

type StepOutcome =
  | { kind: "needs_http"; request: { url: string; headers: [string, string][]; body: unknown } }
  | { kind: "terminal" }
  | { kind: "parked"; next_due_unix_ms: number | null }
  | { kind: "failed"; message: string };

function constantTimeEqual(left: string, right: string): boolean {
  let diff = left.length ^ right.length;
  const max = Math.max(left.length, right.length);
  for (let i = 0; i < max; i += 1) {
    diff |= (left.charCodeAt(i) || 0) ^ (right.charCodeAt(i) || 0);
  }
  return diff === 0;
}

function requestBearerToken(request: Request): string | undefined {
  const authorization = request.headers.get("authorization") ?? "";
  if (authorization.toLowerCase().startsWith("bearer ")) {
    return authorization.slice("bearer ".length).trim();
  }
  return request.headers.get("x-whip-control-token") ?? undefined;
}

function controlAuthError(request: Request, env: Env): Response | undefined {
  const expected = env.WHIP_CONTROL_TOKEN?.trim();
  if (!expected) {
    return Response.json({ error: "WHIP_CONTROL_TOKEN is required" }, { status: 503 });
  }
  const actual = requestBearerToken(request) ?? "";
  if (!constantTimeEqual(actual, expected)) {
    return Response.json({ error: "unauthorized" }, { status: 401 });
  }
  return undefined;
}

function requestBodyTooLarge(request: Request): Response | undefined {
  const declared = request.headers.get("content-length");
  if (declared && Number(declared) > MAX_BOOTSTRAP_BYTES) {
    return Response.json({ error: "request body too large" }, { status: 413 });
  }
  return undefined;
}

async function readJsonBody(request: Request): Promise<Record<string, unknown> | Response> {
  const early = requestBodyTooLarge(request);
  if (early) {
    return early;
  }
  const text = await request.text();
  if (new TextEncoder().encode(text).length > MAX_BOOTSTRAP_BYTES) {
    return Response.json({ error: "request body too large" }, { status: 413 });
  }
  try {
    const parsed = JSON.parse(text || "{}");
    if (parsed && typeof parsed === "object" && !Array.isArray(parsed)) {
      return parsed as Record<string, unknown>;
    }
    return Response.json({ error: "JSON body must be an object" }, { status: 400 });
  } catch (error) {
    return Response.json({ error: `invalid JSON body: ${error instanceof Error ? error.message : String(error)}` }, { status: 400 });
  }
}

function isSentinelRoute(requestUrl: string, sentinelBase: string, suffix: "/exec" | "/turn"): boolean {
  const request = new URL(requestUrl);
  const sentinel = new URL(sentinelBase);
  const basePath = sentinel.pathname.replace(/\/$/, "");
  const expectedPath = `${basePath}${suffix}`;
  return request.origin === sentinel.origin && request.pathname === expectedPath;
}

function redactedUrlForLog(url: string): string {
  try {
    const parsed = new URL(url);
    return `${parsed.origin}${parsed.pathname}`;
  } catch {
    return "<invalid-url>";
  }
}

// Perform one suspended HTTP round and marshal the result back for `step`.
// Requests targeting the executor sentinel host are routed to a
// getRandom-picked container in the EXECUTOR pool instead of the network.
async function performFetch(
  req: {
    url: string;
    headers: [string, string][];
    body: unknown;
  },
  env?: Env,
): Promise<string> {
  try {
    const init = {
      method: "POST",
      headers: Object.fromEntries(req.headers),
      body: JSON.stringify(req.body),
    };
    const executorHost = env?.WHIP_EXECUTOR_URL;
    const turnHost = env?.WHIP_TURN_URL;
    let resp: Response;
    if (turnHost && isSentinelRoute(req.url, turnHost, "/turn")) {
      // Class-B: a per-turn container (1:1), addressed by the turn id in the
      // whip-turn/1 request body. The blocking POST /turn answers with the
      // final outcome; a re-sent start re-attaches idempotently.
      const turnId = String((req.body as { turn_id?: string })?.turn_id ?? "turn");
      const container = env.EXECUTOR.get(env.EXECUTOR.idFromName(`turn-${turnId}`));
      resp = await container.fetch(req.url, init);
    } else if (executorHost && isSentinelRoute(req.url, executorHost, "/exec")) {
      resp = await (await getRandom(env.EXECUTOR, EXECUTOR_POOL_SIZE)).fetch(req.url, init);
    } else {
      resp = await fetch(req.url, init);
    }
    return JSON.stringify({ status: resp.status, body: await resp.json() });
  } catch (error) {
    // The core maps `{"error": ...}` to a TransportError terminal.
    console.log(`performFetch failed for ${redactedUrlForLog(req.url)}: ${error instanceof Error ? error.stack ?? error.message : String(error)}`);
    return JSON.stringify({ error: error instanceof Error ? error.message : String(error) });
  }
}

interface Bootstrap {
  program: string;
  input: string;
  principal: string;
}

export class WorkflowInstance implements DurableObject {
  constructor(
    private ctx: DurableObjectState,
    private env: Env,
  ) {}

  // POST /start { program, input, principal } -- create + drive to the first
  // suspension or terminal. Subsequent external events / alarms re-enter and drive
  // further; the durable state is entirely in DO SQLite.
  async fetch(request: Request): Promise<Response> {
    if (request.method !== "POST") {
      return Response.json({ error: "method not allowed" }, { status: 405 });
    }
    const authError = controlAuthError(request, this.env);
    if (authError) {
      return authError;
    }
    const parsed = await readJsonBody(request);
    if (parsed instanceof Response) {
      return parsed;
    }
    const body = parsed as Partial<Bootstrap> & { command?: string; cut_id?: string };
    // Operator commands (P3): checkpoint / restore an existing instance. The
    // worker's only verb dispatch — everything else is the start/poke bootstrap.
    if (body.command === "checkpoint" || body.command === "restore") {
      const existing = await this.ctx.storage.get<Bootstrap>("bootstrap");
      if (!existing) {
        return Response.json({ error: "no instance to command" }, { status: 400 });
      }
      if (!body.cut_id) {
        return Response.json({ error: `${body.command} requires cut_id` }, { status: 400 });
      }
      const instance = this.makeInstance(existing);
      try {
        const report =
          body.command === "checkpoint"
            ? instance.checkpoint(body.cut_id)
            : instance.restore(body.cut_id);
        return new Response(report, { headers: { "content-type": "application/json" } });
      } catch (error) {
        // A refusal (unknown cut / dangling manifest) or non-quiescent
        // checkpoint mutates nothing and returns a 400.
        return Response.json({ error: String(error) }, { status: 400 });
      }
    }
    const program = typeof body.program === "string" ? body.program : undefined;
    const input = typeof body.input === "string" ? body.input : undefined;
    const principal = typeof body.principal === "string" ? body.principal : undefined;
    let bootstrap: Bootstrap | undefined;
    if (program) {
      const existing = await this.ctx.storage.get<Bootstrap>("bootstrap");
      if (existing) {
        return Response.json({ error: "instance already bootstrapped; use a new id" }, { status: 409 });
      }
      bootstrap = {
        program,
        input: input ?? "{}",
        principal: principal ?? "local/Workflow",
      };
      // Persisted so an alarm (or a body-less poke) can rehydrate the wasm
      // instance without the caller resupplying the program.
      await this.ctx.storage.put("bootstrap", bootstrap);
    } else {
      bootstrap = await this.ctx.storage.get<Bootstrap>("bootstrap");
    }
    if (!bootstrap) {
      return Response.json({ error: "no program: POST { program, input, principal } first" }, { status: 400 });
    }
    const result = await this.drive(bootstrap);
    return Response.json(result);
  }

  // The DO's single wake-up (DR-0033 Phase 6): a parked instance with pending
  // timers/deadlines scheduled this; re-enter and drive — the due-time pass
  // fires the timers, the rule pass sees the facts, and the run continues.
  async alarm(): Promise<void> {
    const bootstrap = await this.ctx.storage.get<Bootstrap>("bootstrap");
    if (bootstrap) {
      const result = await this.drive(bootstrap);
      console.log(`alarm fired: drove instance to ${result.status} (${result.outcome})`);
    }
  }

  // Build (get-or-create) the wasm instance from the persisted bootstrap, wiring
  // the effect ports from DO secrets/bindings. Shared by `drive` (the step loop)
  // and the operator command path (checkpoint/restore).
  private makeInstance(bootstrap: Bootstrap): WasmDurableInstance {
    ensureSchema(this.ctx.storage.sql);
    const bridge = makeBridge(this.ctx.storage.sql);
    // Provider creds from DO secrets (optional; store-only workflows omit both).
    // Same JSON shape for coerce and agent turns; a live agent turn with tools
    // also needs a tool-executor sidecar (the remaining async-tool seam).
    const anthropicConfig = (model: string, maxTokens: number) =>
      this.env.ANTHROPIC_API_KEY
        ? JSON.stringify({
            provider: "anthropic",
            base_url: this.env.WHIP_PROVIDER_BASE_URL ?? "https://api.anthropic.com",
            api_key: this.env.ANTHROPIC_API_KEY,
            model,
            max_tokens: maxTokens,
          })
        : undefined;
    const coerceConfig = anthropicConfig("claude-3-5-sonnet-latest", 1024);
    const agentConfig = anthropicConfig("claude-3-5-sonnet-latest", 4096);
    // Class-A exec sidecar wiring (compute plane P8): the executor URL comes
    // from the environment; script env references resolve against DO secrets.
    const execConfig = this.env.WHIP_EXECUTOR_URL
      ? JSON.stringify({
          base_url: this.env.WHIP_EXECUTOR_URL,
          environment_epoch: this.env.WHIP_COMPUTE_ENV_HASH,
          env: {
            ...(this.env.ANTHROPIC_API_KEY ? { ANTHROPIC_API_KEY: this.env.ANTHROPIC_API_KEY } : {}),
            ...(this.env.OPENAI_API_KEY ? { OPENAI_API_KEY: this.env.OPENAI_API_KEY } : {}),
          },
          auth_token: this.env.WHIP_EXECUTOR_TOKEN,
        })
      : undefined;
    // Class-B turn containers: forward the provider config into the container
    // (the container runs the model calls itself); fixture when no key is set.
    const turnConfig = this.env.WHIP_TURN_URL
      ? JSON.stringify({
          base_url: this.env.WHIP_TURN_URL,
          provider: this.env.ANTHROPIC_API_KEY
            ? {
                provider: "anthropic",
                base_url: this.env.WHIP_PROVIDER_BASE_URL ?? "https://api.anthropic.com",
                api_key: this.env.ANTHROPIC_API_KEY,
                model: "claude-3-5-sonnet-latest",
                max_tokens: 4096,
              }
            : { provider: "fixture" },
          auth_token: this.env.WHIP_EXECUTOR_TOKEN,
        })
      : undefined;
    return WasmDurableInstance.create(
      bridge,
      bootstrap.program,
      bootstrap.input,
      bootstrap.principal,
      coerceConfig,
      agentConfig,
      this.env.WHIP_PROJECT_CONTEXT_JSON,
      execConfig,
      this.env.WHIP_SCRIPT_CAPABILITIES_JSON,
      turnConfig,
    );
  }

  private async drive(bootstrap: Bootstrap): Promise<{ status: string; outcome: string }> {
    const instance = this.makeInstance(bootstrap);

    // The sans-IO loop: step -> maybe fetch -> step, until a terminal or a park.
    // Each `fetch` is one provider round; the core parses the response and
    // continues (or, on the final round, settles). A production shell persists
    // after each round so the object can hibernate; here we run the loop within
    // one request for clarity.
    let responseJson: string | undefined = undefined;
    for (;;) {
      const outcome = JSON.parse(instance.step(responseJson, Date.now())) as StepOutcome;
      if (outcome.kind === "needs_http") {
        responseJson = await performFetch(outcome.request, this.env);
        continue;
      }
      if (outcome.kind === "parked" && outcome.next_due_unix_ms != null) {
        // The instance holds pending timers/deadlines: schedule the DO's
        // single alarm at the earliest one (the Alarms seam, live).
        await this.ctx.storage.setAlarm(outcome.next_due_unix_ms);
      }
      return { status: instance.status(), outcome: outcome.kind };
    }
  }
}

// The Worker entrypoint routes to the addressed instance object.
export default {
  async fetch(request: Request, env: Env, _ctx: ExecutionContext): Promise<Response> {
    const url = new URL(request.url);
    if (url.pathname === "/healthz") {
      return Response.json({ ok: true });
    }
    if (url.pathname !== "/start") {
      return Response.json({ error: "not found" }, { status: 404 });
    }
    if (request.method !== "POST") {
      return Response.json({ error: "method not allowed" }, { status: 405 });
    }
    const authError = controlAuthError(request, env) ?? requestBodyTooLarge(request);
    if (authError) {
      return authError;
    }
    const id = url.searchParams.get("id") ?? "default";
    const stub = env.WORKFLOW_INSTANCE.get(env.WORKFLOW_INSTANCE.idFromName(id));
    return stub.fetch(request);
  },
} satisfies ExportedHandler<Env>;
